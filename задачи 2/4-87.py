# Предположим, что у вас есть матрица matrix
matrix = [
    [1, 0, 3],
    [0, 5, 0],
    [7, 0, 9]
]

# Инициализация счетчика
count = 0

# Перебор строк матрицы
for row in matrix:
    # Перебор элементов в строке
    for element in row:
        # Проверка на ненулевой элемент
        if element != 0:
            count += 1  # Увеличение счетчика

# Вывод результата
print(f"Количество ненулевых элементов в матрице: {count}")
