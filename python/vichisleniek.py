import math

# Данные
m = 9.11e-31  # масса электрона, кг
h_bar = 1.0545718e-34  # постоянная Планка, Дж*с
E = 3 * 1.60218e-19  # энергия частицы, Дж
U = 5 * 1.60218e-19  # высота барьера, Дж
a = 0.5e-9  # ширина барьера, м

# Рассчёт ∆U (U - E)
delta_U = U - E

# Вычисление κ
kappa = math.sqrt(2 * m * delta_U) / h_bar

# Вероятность туннелирования T
T = math.exp(-2 * kappa * a)
T

print(f"Вероятность туннелирования составляет: {T:.5f}")
