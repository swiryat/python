import math

# Заданные параметры
h1 = 10          # Высота антенны РЛС в метрах
D = 10           # Заданная дальность обнаружения в километрах
k = 4.12         # Коэффициент для расчёта радиовидимости

# Вычисляем первую часть: sqrt(h1)
sqrt_h1 = math.sqrt(h1)

# Вычисляем сумму корней, которая должна равняться D/k
sum_sqrt = D / k

# Находим sqrt(h2) как разность между суммой корней и sqrt(h1)
sqrt_h2 = sum_sqrt - sqrt_h1

# Вычисляем минимальную высоту цели h2, возводя sqrt_h2 в квадрат
h2 = sqrt_h2 ** 2

print("При высоте антенны РЛС {} м и дальности обнаружения {} км, минимальная высота цели составляет {:.2f} м".format(h1, D, h2))
