# Получение натурального числа от пользователя
num = int(input("Введите натуральное число: "))

# Проверка на натуральность числа
if num <= 0:
    print("Ошибка: Введите натуральное число (больше нуля).")
else:
    # Преобразование числа в строку и подсчет количества цифр
    num_str = str(num)
    digit_count = len(num_str)
    
    # Вывод результата
    print(f"Количество цифр в числе {num} равно {digit_count}.")
