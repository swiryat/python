# Создайте список чисел в интервале (1, 20) с шагом 3
my_list = list(range(1, 20, 3))

# Напечатайте длину списка
print("Длина списка:", len(my_list))

# Выведите все элементы списка, начиная со второй позиции
print("Элементы, начиная со второй позиции:", my_list[1:])

# Выведите все четные элементы списка
print("Четные элементы списка:", [x for x in my_list if x % 2 == 0])

# Выведите все элементы списка, делящиеся на 5 без остатка
divisible_by_5 = [x for x in my_list if x % 5 == 0]
if divisible_by_5:
    print("Элементы, делящиеся на 5 без остатка:", divisible_by_5)
else:
    print("Элементов, делящихся на 5, нет")

# Отсортируйте список в порядке убывания
my_list.sort(reverse=True)
print("Список, отсортированный в порядке убывания:", my_list)

# Рассчитайте квадраты элементов исходного списка
squares = list(map(lambda x: pow(x, 2), my_list))
print("Квадраты элементов списка:", squares)

# Выведите элементы списка в обратном порядке с помощью срезов
print("Элементы списка в обратном порядке:", my_list[::-1])

# Рассчитайте выборочное среднее для элементов списка
n = len(my_list)
mean = sum(my_list) / n
print("Выборочное среднее элементов списка:", mean)
